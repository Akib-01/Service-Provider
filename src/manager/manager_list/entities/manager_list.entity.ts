import { Column, Entity, PrimaryGeneratedColumn } from 'typeorm';
@Entity('Manager_list')
export class ManagerList {
  @PrimaryGeneratedColumn()
  id: number;
  @Column()
  name: string;
  @Column()
  phone: string;
  @Column()
  email: string;
  @Column()
  DOB: string;
  @Column()
  username: string;
  @Column()
  password: string;
}
